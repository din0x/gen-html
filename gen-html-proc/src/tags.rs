pub fn is_self_closing(name: &str) -> Option<bool> {
    ELEMENTS.contains(&name).then_some(VOID.contains(&name))
}

macro_rules! void_elements {
    ($($tag:ident)*) => {
        const VOID: [&str; 13] = [$(stringify!($tag),)*];
    };
}

